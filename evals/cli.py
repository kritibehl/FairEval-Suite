import typer

from .runner import run_suite
from evals.compare.diff import compare_reports

app = typer.Typer(help="GenAI Evaluation Platform CLI")


@app.command()
def run(
    suite: str = typer.Option(..., help="Evaluation suite name (e.g., rag_basic)"),
    model: str = typer.Option("mock", help="Model identifier"),
    out_dir: str = typer.Option(".", help="Output directory for runs/ and reports/"),
):
    """
    Run an evaluation suite and write run/report artifacts to runs/ and reports/.
    """
    dataset_path = f"datasets/{suite}/cases.jsonl"
    res = run_suite(
        suite_name=suite,
        dataset_path=dataset_path,
        model_name=model,
        out_dir=out_dir,
    )
    typer.echo(res)


@app.command()
def report(
    latest: bool = typer.Option(
        True, help="Generate report for latest run (v1: already generated in run)"
    ),
):
    """
    v1: report JSON is already generated by `run`.
    (We’ll add HTML generation in a later commit.)
    """
    if latest:
        typer.echo("v1: reports are generated during `eval run`. Check reports/*.json")
    else:
        typer.echo("v1: not implemented")


@app.command()
def run(
    suite: str = typer.Option(..., help="Evaluation suite name (e.g., rag_basic)"),
    model: str = typer.Option("mock", help="Model identifier"),
    out_dir: str = typer.Option(".", help="Output directory for runs/ and reports/"),
    max_workers: int = typer.Option(4, help="Max parallel workers for case execution"),
):
    dataset_path = f"datasets/{suite}/cases.jsonl"
    res = run_suite(
        suite_name=suite,
        dataset_path=dataset_path,
        model_name=model,
        out_dir=out_dir,
        max_workers=max_workers,
    )
    typer.echo(res)


if __name__ == "__main__":
    app()
